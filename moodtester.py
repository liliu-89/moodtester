mood = input("Enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")
    
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("Calm down, Bro!")
elif mood == "relaxed":
    print("Nice to hear")
    
else:
    print("I don't recognize this mood")
